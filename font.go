type offset struct {
	xoffset  float32
	yoffset  float32
	xadvance float32
}

// A Font is bitmap font that can be rendered with a Batch.
type Font struct {
	texture *Texture
	regions []*Region
	offsets []*offset
	mapping map[rune]int
}

// NewBitmapFont constructs a new bitmap font from the bmfont format.
// fnt and img should either be string paths to the font and image
// files respectively or io.Reader's of the same.
func NewBitmapFont(img interface{}, fnt interface{}) *Font {
	var reader *bufio.Reader
	switch data := fnt.(type) {
	default:
		log.Fatal("NewBitmapTexture needs a string or io.Reader")
	case string:
		file, err := os.Open(data)
		if err != nil {
			log.Fatal(err)
		}
		defer file.Close()
		reader = bufio.NewReader(file)
	case io.Reader:
		reader = bufio.NewReader(data)
	}

	texture := NewTexture(img)

	font := new(Font)
	font.regions = make([]*Region, 0)
	font.offsets = make([]*offset, 0)
	font.mapping = make(map[rune]int)
	font.texture = texture

	index := 0
	for {
		line, err := reader.ReadString('\n')
		if err == io.EOF {
			break
		}
		if err != nil {
			log.Fatal(err)
		}
		groups := strings.Split(line, " ")
		switch groups[0] {
		default:
		case "char":
			char := make(map[string]int64)
			for i := 1; i < len(groups); i++ {
				if strings.Contains(groups[i], "=") {
					k, v := split(strings.TrimSpace(groups[i]))
					num, _ := strconv.ParseInt(v, 10, 64)
					char[k] = num
				}
			}

			font.mapping[rune(char["id"])] = index
			os := &offset{float32(char["xoffset"]), float32(char["yoffset"]), float32(char["xadvance"])}
			font.offsets = append(font.offsets, os)
			r := NewRegion(texture, int(char["x"]), int(char["y"]), int(char["width"]), int(char["height"]))
			font.regions = append(font.regions, r)

			index += 1
		}
	}

	return font
}

func split(s string) (string, string) {
	strs := strings.Split(s, "=")
	return strs[0], strs[1]
}

// NewGridFont constructs a new bitmap font from an image of equally
// spaced glyphs (cellWidth x cellHeight) which are laid out from
// left to right, top to bottom, using the runes in maps. img should
// either be a string path the image or an io.Reader.
func NewGridFont(img interface{}, cellWidth, cellHeight int, maps string) *Font {
	texture := NewTexture(img)

	font := new(Font)
	font.regions = make([]*Region, 0)
	font.offsets = make([]*offset, 0)
	font.mapping = make(map[rune]int)
	font.texture = texture

	i := 0
	for _, v := range maps {
		font.mapping[v] = i
		i++
	}

	os := &offset{0, 0, float32(cellWidth)}
	for y := 0; y < texture.Height()/cellHeight; y++ {
		for x := 0; x < texture.Width()/cellWidth; x++ {
			font.offsets = append(font.offsets, os)
			r := NewRegion(texture, x*cellWidth, y*cellHeight, cellWidth, cellHeight)
			font.regions = append(font.regions, r)
		}
	}

	return font
}

// NewTrueTypeFont constructs a bitmap font from a .ttf file using
// only the runes specified in maps, at the size indicated by scale.
// fnt can either be a string path or an io.Reader.
func NewTrueTypeFont(fnt interface{}, scale int, maps string) *Font {
	var reader io.Reader
	switch data := fnt.(type) {
	default:
		log.Fatal("NewTTFont needs a string or io.Reader")
	case string:
		file, err := os.Open(data)
		if err != nil {
			log.Fatal(err)
		}
		defer file.Close()
		reader = file
	case io.Reader:
		reader = data
	}

	font := new(Font)
	font.regions = make([]*Region, 0)
	font.offsets = make([]*offset, 0)
	font.mapping = make(map[rune]int)

	raw, err := ioutil.ReadAll(reader)
	if err != nil {
		log.Fatal(err)
	}

	ttf, err := truetype.Parse(raw)
	if err != nil {
		log.Fatal(err)
	}

	gc := int32(len(maps))
	glyphsPerRow := int32(16)
	glyphsPerCol := (gc / glyphsPerRow) + 1

	gb := ttf.Bounds(int32(scale))
	gw := (gb.XMax - gb.XMin)
	gh := (gb.YMax - gb.YMin) + 5
	iw := pow2(uint32(gw * glyphsPerRow))
	ih := pow2(uint32(gh * glyphsPerCol))

	rect := image.Rect(0, 0, int(iw), int(ih))
	img := image.NewRGBA(rect)

	c := freetype.NewContext()
	c.SetDPI(72)
	c.SetFont(ttf)
	c.SetFontSize(float64(scale))
	c.SetClip(img.Bounds())
	c.SetDst(img)
	c.SetSrc(image.White)

	texture := NewTexture(img)

	var gx, gy int32
	i := 0
	for _, v := range maps {
		font.mapping[v] = i

		index := ttf.Index(v)
		metric := ttf.HMetric(int32(scale), index)

		os := &offset{float32(0), float32(-5), float32(metric.AdvanceWidth)}
		font.offsets = append(font.offsets, os)
		r := NewRegion(texture, int(gx), int(gy), int(gw), int(gh))
		font.regions = append(font.regions, r)

		pt := freetype.Pt(int(gx), int(gy)+int(scale))
		c.DrawString(string(v), pt)

		i++

		if i%16 == 0 {
			gx = 0
			gy += gh
		} else {
			gx += gw
		}
	}

	font.texture = NewTexture(img)
	for _, r := range font.regions {
		r.texture = font.texture
	}

	return font
}

func (f *Font) mapRune(ch rune) (int, bool) {
	if f.mapping == nil {
		return int(ch), true
	}
	position, ok := f.mapping[ch]
	return position, ok
}

func (f *Font) Put(batch *Batch, r rune, x, y float32, color *Color) {
	i, ok := f.mapRune(r)
	if ok {
		region := f.regions[i]
		offset := f.offsets[i]
		batch.Draw(region, x+offset.xoffset, y+offset.yoffset, 0, 0, 1, 1, 0, color)
	}
}

// Print renders some text with the first letter's top left corner at
// x, y using the given color. If color == nil, the given batch's
// current color will be used. If the string contains a rune that is
// not in the font, that rune will be skipped.
func (f *Font) Print(batch *Batch, t interface{}, x, y float32, color *Color) {
	text := ""
	switch t := t.(type) {
	default:
		text = fmt.Sprintf("%v", t)
	case string:
		text = t
	}

	xx := x
	for _, v := range text {
		i, ok := f.mapRune(v)
		if ok {
			region := f.regions[i]
			offset := f.offsets[i]
			batch.Draw(region, xx+offset.xoffset, y+offset.yoffset, 0, 0, 1, 1, 0, color)
			xx += offset.xadvance
		}
	}
}

// Texture returns the backing texture of the font. This can be useful
// for setting the filter or wrap modes which default to linear and
// wrap to edge respectively.
func (f *Font) Texture() *Texture {
	return f.texture
}

func pow2(x uint32) uint32 {
	x--
	x |= x >> 1
	x |= x >> 2
	x |= x >> 4
	x |= x >> 8
	x |= x >> 16
	return x + 1
}

